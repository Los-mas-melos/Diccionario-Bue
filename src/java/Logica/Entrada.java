

package Logica;

public class Entrada {

    public String español;
    public String bue;

    public Entrada(String español, String bue) {
        this.español = español;
        this.bue = bue;
    }

    public String getEspañol() {
        return español;
    }

    public void setEspañol(String español) {
        this.español = español;
    }

    public String getBue() {
        return bue;
    }

    public void setBue(String bue) {
        this.bue = bue;
    }
}
